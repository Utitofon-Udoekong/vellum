#!/usr/bin/env node
import { writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { BarretenbergSync, Fr } from "@aztec/bb.js";

const TREE_DEPTH = 20;
const RECIPIENT_PUBKEY_HEX =
  "0f28a41a5b3abb2998e63e8276df7dc11dba708f69c3f6c705003712bafe20af";
const pk = Buffer.from(RECIPIENT_PUBKEY_HEX, "hex");
let pubkeyLo = 0n;
let pubkeyHi = 0n;
for (let i = 0; i < 16; i++) pubkeyLo = (pubkeyLo << 8n) | BigInt(pk[i]);
for (let i = 16; i < 32; i++) pubkeyHi = (pubkeyHi << 8n) | BigInt(pk[i]);

const amount = 100n;
const salt = 42n;
const privKey = 7n;

const api = await BarretenbergSync.initSingleton();

function hash2(a, b) {
  return api.poseidon2Hash([new Fr(a), new Fr(b)]);
}

function hash3(a, b, c) {
  return api.poseidon2Hash([new Fr(a), new Fr(b), new Fr(c)]);
}

function field(fr) {
  return BigInt(fr.toString());
}

const zeroes = [0n];
for (let i = 0; i < TREE_DEPTH; i++) {
  zeroes.push(field(hash2(zeroes[i], zeroes[i])));
}

const recipientId = field(hash2(pubkeyLo, pubkeyHi));
const nullifierHash = hash2(privKey, salt);
const leaf = hash3(recipientId, amount, salt);

let cur = leaf;
const siblings = [];
const bits = [];
for (let i = 0; i < TREE_DEPTH; i++) {
  bits.push(0);
  siblings.push(zeroes[i]);
  cur = hash2(field(cur), zeroes[i]);
}
const root = cur;

const siblingLines = siblings.map((s) => `"${s}"`).join(", ");
const bitLines = bits.map(() => '"0"').join(", ");
const toml = `# Auto-generated by apps/web/scripts/populate-withdraw-prover.mjs
root = "${root.toString()}"
nullifier_hash = "${nullifierHash.toString()}"
amount = "${amount}"
recipient_id = "${recipientId}"
pubkey_lo = "${pubkeyLo.toString()}"
pubkey_hi = "${pubkeyHi.toString()}"
priv_key = "${privKey}"
salt = "${salt}"

path_siblings = [
  ${siblingLines}
]
path_bits = [
  ${bitLines}
]
`;

const out = resolve(dirname(fileURLToPath(import.meta.url)), "../../../circuits/withdraw/Prover.toml");
writeFileSync(out, toml);
console.log(`Wrote ${out}`);
console.log(`recipient_id: ${recipientId}`);
console.log(`leaf commitment hex: ${field(leaf).toString(16).padStart(64, "0")}`);
